package com.example.myexe;

public class XemBoi {
    public int icon;
    public String title;

    public XemBoi(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public XemBoi() {
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
